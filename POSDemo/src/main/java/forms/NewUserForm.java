    private void btnAddUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddUserActionPerformed
                       
        String password1 = tbxPassword.getText().trim();
        String password2 = tbxComfirmPassword.getText().trim();
        if (!password1.equals(password2)) {
            JOptionPane.showMessageDialog(this, "Password does not match.","VALIDATION WARNING",JOptionPane.WARNING_MESSAGE);
            tbxPassword.setText("");
            tbxComfirmPassword.setText("");
            tbxPassword.requestFocus();
            return;
        }
        
        User.UserInfo ui = new User.UserInfo();
        ui.id = 0;
        ui.username = tbxUsername.getText().trim();
        ui.password = tbxPassword.getText().trim();
        ui.email = tbxEmail.getText().trim();
        ui.phone = tbxPhone.getText().trim();
        ui.level = (cmbUserLevel.getSelectedIndex()*30) + 1;
        
        ValidationResult vr = validateUser(ui);
        
        if (vr.result) {
            // Encrypt password
            ui.password = encrypt(ui.password, SECRET_KEY);   
            
            User newUser = User.addUser(ui);
            if(newUser != null){
                Log.LogInfo li = new Log.LogInfo();
                li.id = 0;
                li.log_date = getCurrentDateTimeFormatted();
                li.user = CURRENT_USER.username;
                li.category = "SYSTEM LOG";
                li.event = "ADD USER";            
                li.details = "User '"+CURRENT_USER.username+"' (User Level: "+getUserLevel(CURRENT_USER.level)+") \n"
                        + "ADD NEW USER\n"
                        + " User[\n"
                        + "  ID: "+newUser.getId()+",\n"
                        + "  Username: "+newUser.getUsername()+",\n"
                        + "  Password: <***SECRET***>,\n"
                        + "  Email: "+newUser.getEmail()+",\n"
                        + "  Phone: "+newUser.getPhone()+",\n"
                        + "  UserLevel: "+getUserLevel(newUser.getLevel())+",\n"
                        + "  Result: SUCCESS\n"
                        + " ]";
                Log.addLog(li);
                
                JOptionPane.showMessageDialog(this, "Add new User successful.","SUCCESSFUL.",JOptionPane.INFORMATION_MESSAGE);                
                setFrameState(frameState.POST_EDIT);                
                tbxUsername.requestFocus();
            }else{
                JOptionPane.showMessageDialog(this, "Add new User failed.", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(this, vr.message, "VALIDATION WARNING", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_btnAddUserActionPerformed

    private void tbxUsernameFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tbxUsernameFocusLost
        
        String username = tbxUsername.getText().trim();
        if (username.length() >= 4) {
            User user = User.isExist("username='"+username+"'");
            if(user != null) {
                JOptionPane.showMessageDialog(this, "Username '"+user.getUsername()+"' is already exists.", "Error", JOptionPane.ERROR_MESSAGE);
                tbxUsername.setText("");
                tbxUsername.requestFocus();
            }            
        }
        
    }//GEN-LAST:event_tbxUsernameFocusLost

