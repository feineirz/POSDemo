    private ValidationResult validateUser(User.UserInfo ui) {
        
        ValidationResult vr;
        
        vr = validateUsername(ui.username);        
        if (!vr.result) {
            tbxUsername.requestFocus();
            return vr;
        }
        
        vr = validatePassword(ui.password);        
        if (!vr.result) {
            tbxPassword.setText("");
            tbxComfirmPassword.setText("");
            tbxPassword.requestFocus();
            return vr;
        }
        
        vr = validateEmail(ui.email);        
        if (!vr.result) {
            tbxEmail.setText("");
            tbxEmail.requestFocus();
            return vr;
        }
        
        vr.result = true;
        vr.message = "PASSED";
        return vr;
        
    }

